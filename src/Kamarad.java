public class Kamarad {
    private String jmeno;
    private int rokNarozeni;

    public Kamarad(String jmeno, int rokNarozeni) {
        this.jmeno = jmeno;
        this.rokNarozeni = rokNarozeni;
    }
    public String getJmeno() {
        return jmeno;
    }
    public int getRokNarozeni() {
        return rokNarozeni;
    }

}
